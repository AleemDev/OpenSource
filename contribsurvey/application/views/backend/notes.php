<div id="qtip_qtype" class="qtip_notes">
	<span><b>Notes<i>!</i></b></span>
	<p><b>Question types:</b></p>
	<ul>
		<li><span>Single</span>: a question with a list of options presented as radio buttons from which respondents can choose one</li>
		<li><span>Dropdown</span>: a question with a list of options presented as a dropdown menu from which respondents can choose one</li>
		<li><span>Multi</span>: a question with a list of options (tick boxes) from which a respondent can choose as many as apply</li>
		<li><span>Smallbox</span>: a one-line text box for short written answers</li>
		<li><span>Bigbox</span>: a multi-line text box with scroll bar for longer written answers</li>
		<li><span>Pagebreak</span>: marks the end of the current page of questions</li>
		<li><span>Info</span>: a text-only message or instruction</li>
	</ul>
</div>